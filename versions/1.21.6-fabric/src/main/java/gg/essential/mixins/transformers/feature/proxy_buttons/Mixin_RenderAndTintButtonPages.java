package gg.essential.mixins.transformers.feature.proxy_buttons;

import gg.essential.gui.proxies.TintVanillaButtonsEffect;
import net.minecraft.client.gui.render.GuiRenderer;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(GuiRenderer.class)
public abstract class Mixin_RenderAndTintButtonPages {
    @Inject(method = "prepare", at = @At("HEAD"))
    private void prepareTintedButtons(CallbackInfo ci) {
        TintVanillaButtonsEffect.Page.renderAll();
    }
}
