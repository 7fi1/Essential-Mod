package gg.essential.mixins.transformers.feature.gl_debug;

import com.llamalad7.mixinextras.injector.wrapoperation.Operation;
import com.llamalad7.mixinextras.injector.wrapoperation.WrapOperation;
import gg.essential.util.GlDebug;
import net.minecraft.client.Minecraft;
import org.lwjgl.LWJGLException;
import org.lwjgl.opengl.ContextAttribs;
import org.lwjgl.opengl.Display;
import org.lwjgl.opengl.PixelFormat;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;

@Mixin(Minecraft.class)
public class Mixin_EnableDebugContext {
    @WrapOperation(method = "createDisplay", at = @At(value = "INVOKE", target = "Lorg/lwjgl/opengl/Display;create(Lorg/lwjgl/opengl/PixelFormat;)V", remap = false))
    private void createDebugContext(PixelFormat pixelFormat, Operation<Void> operation) throws LWJGLException {
        if (GlDebug.ENABLED) {
            Display.create(pixelFormat, new ContextAttribs().withDebug(true));
        } else {
            operation.call(pixelFormat);
        }
    }
}
